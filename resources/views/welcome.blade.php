    // Función para activar la animación cuando el elemento entra en la vista
    function activateAnimation(entries, observer) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animated');
                observer.unobserve(entry.target);
            }
        });
    }

    // Crear un observer para los elementos con la clase 'animate-on-scroll'
    const animateOnScrollElements = document.querySelectorAll('.animate-on-scroll');
    const observer = new IntersectionObserver(activateAnimation, { threshold: 0.5 });

    // Observar cada elemento con la clase 'animate-on-scroll'
    animateOnScrollElements.forEach(element => {
        observer.observe(element);
    });
</script>

