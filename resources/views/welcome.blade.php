        function cargarImagenesDesdeAPI() {
            fetch('https://picsum.photos/v2/list')
                .then(response => response.json())
                .then(data => {
                    const carouselInner = document.querySelector('.carousel-inner');
                    
                    for (let i = 0; i < data.length; i += 3) {
                        const carouselItem = document.createElement('div');
                        carouselItem.classList.add('carousel-item');
                        if (i === 0) {
                            carouselItem.classList.add('active');
                        }
                        
                        const usedIndexes = [];
                        const carouselItemInner = document.createElement('div');
                        carouselItemInner.classList.add('carousel-item-inner', 'row');
                        
                        for (let j = i; j < i + 3 && j < data.length; j++) {
                            const imgElement = document.createElement('img');
                            imgElement.src = data[j].download_url;
                            imgElement.alt = `Imagen ${j + 1}`;
                            imgElement.classList.add('d-block', 'w-100');
                            
                            const authorElement = document.createElement('p');
                            authorElement.textContent = `Autor: ${data[j].author}`;
                            authorElement.classList.add('author');
                            
                            const col = document.createElement('div');
                            col.classList.add('col');
                            col.appendChild(imgElement);
                            col.appendChild(authorElement);
                            carouselItemInner.appendChild(col);
                            
                            usedIndexes.push(j);
                        }
                        
                        carouselItem.appendChild(carouselItemInner);
                        carouselInner.appendChild(carouselItem);
                    }
                })
                .catch(error => console.error('Error al cargar las imágenes desde la API:', error));
        }

        document.addEventListener('DOMContentLoaded', cargarImagenesDesdeAPI);
    </script>

